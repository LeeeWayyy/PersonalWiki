export function getLang() {
  try {
    const l = localStorage.getItem(LANG_KEY);
    if (l === 'en' || l === 'zh') return l;
  } catch {} // server / no storage → default
  return DEFAULT_LANG;
}

export function t(key, args, lang) {
  const pair = D[key];
  let s = pair ? pair[(lang || getLang()) === 'en' ? 0 : 1] : key;
  if (args) for (const [k, v] of Object.entries(args)) s = s.replaceAll(`{${k}}`, String(v));
  return s;
}
